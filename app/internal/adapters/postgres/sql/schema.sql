-- Create a function that generates a UUID-compatible string
CREATE OR REPLACE FUNCTION generate_uuid()
RETURNS uuid AS $$
DECLARE
    v_time timestamp with time zone := clock_timestamp();
    v_epoch bigint;
    v_micro bigint;
    v_random text;
    v_uuid text;
BEGIN
    -- Extract time components with maximum precision
    v_epoch := extract(epoch from v_time)::bigint;
    v_micro := mod(extract(microseconds from v_time)::bigint, 1000000);
    
    -- Generate random components using built-in random()
    v_random := '';
    FOR i IN 1..10 LOOP
        v_random := v_random || lpad(to_hex(floor(random() * 256)::int), 2, '0');
    END LOOP;
    
    -- Construct parts of the UUID using timestamp and randomness
    -- Format: xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx
    -- Where y is 8, 9, a, or b
    v_uuid := lpad(to_hex(v_epoch), 8, '0') || '-' ||
              lpad(to_hex(v_micro), 4, '0') || '-' ||
              '4' || substring(v_random, 1, 3) || '-' ||
              substr('89ab', 1 + (random() * 4)::int % 4, 1) || substring(v_random, 4, 3) || '-' ||
              substring(v_random, 7, 12);
              
    RETURN v_uuid::uuid;
END;
$$ LANGUAGE plpgsql;

